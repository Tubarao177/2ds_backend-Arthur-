export function banhoGato(raca, tipoBanho){
    return `O gato ${raca} está tomando um banho ${tipoBanho}.`;
}

export function tosaGato(raca, tipoTosa){
    return `O gato ${raca} está sendo tosado com ${tipoTosa}.`;
}

export function alimentarGato(raca, tipoComida){
    return `O gato ${raca} está sendo alimentado com ${tipoComida}.`;
}

